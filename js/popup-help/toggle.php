<br /><br />

<span class="title" style="padding:0px 5px 0px 5px;background-color:#CCFCD9;border:1px solid #339966;"><strong>Usage:</strong></span>
<br />
Placing a check in the box will enable toggle switch.  Unchecking the box removes toggle switch.<br /><br />
This is a nifty little javascript which will add a toggle switch just above the visual editor.  It will "quickly" show or hide the editor buttons when clicked.  I decided this is most often used in brief instances, so I decided to make it only during a page "stay".  Once you refresh the page, or leave and come back, all the visual editor button rows will again be displayed by default.

